package org.pradeep.service.messenger.model;

/**
 * @author pradeep
 *
 */
public class Link {
	private String link;
	private String rel;

	/**
	 * @return the link
	 */
	public final String getLink() {
		return link;
	}

	/**
	 * @param link
	 *            the link to set
	 */
	public final void setLink(String link) {
		this.link = link;
	}

	/**
	 * @return the rel
	 */
	public final String getRel() {
		return rel;
	}

	/**
	 * @param rel
	 *            the rel to set
	 */
	public final void setRel(String rel) {
		this.rel = rel;
	}

}
